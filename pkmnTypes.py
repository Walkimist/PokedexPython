class Type:
    def __init__(self, name):
        self.name = name  # str
        self.chart = [self.getChartAttack(), self.getChartDefense()]  # dict list

    def getChartAttack(self):
        c = {}
        for i, t in enumerate(typeNames):
            c[t] = typeChart[typeNames.index(self.name)][i]
        return c

    def getChartDefense(self):
        c = {}
        for i, t in enumerate(typeNames):
            c[t] = typeChart[i][typeNames.index(self.name)]
        return c

    def getAttackEffectiveness(self, other, isAttacking=False):
        if isAttacking:
            return self.chart[0][other]
        return self.chart[1][other]


types = []
for t in typeNames:
    types.append(Type(t))
